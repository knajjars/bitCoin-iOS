import Foundation

protocol CoinManagerDelegate {
    func didFailWithError(_ error: Error) -> Void
    func didUpdateBitCoin(withValue value: Double, withCurrency currency: String) -> Void
}

struct CoinManager {
    
    
    var delegate: CoinManagerDelegate?
    let baseURL = "https://apiv2.bitcoinaverage.com/indices/global/ticker/BTC"
    let currencyArray = ["AUD", "BRL","CAD","CNY","EUR","GBP","HKD","IDR","ILS","INR","JPY","MXN","NOK","NZD","PLN","RON","RUB","SEK","SGD","USD","ZAR"]
    
    
    func getCoinPrice(for currency: String) {
        if let url = URL(string: "\(baseURL)\(currency)") {
            let session = URLSession(configuration: .default)
            let task = session.dataTask(with: url) { (data, response, error) in
                if error != nil {
                    self.delegate?.didFailWithError(error!)
                    return
                }
                
                if let decodedData = self.parseJSON(data!){
                    self.delegate?.didUpdateBitCoin(withValue: decodedData, withCurrency: currency)
                }
            }
            task.resume()
        }
    }
    
    func parseJSON(_ data: Data) -> Double? {
        let decoder = JSONDecoder()
        do {
            let decodedData = try decoder.decode(CoinData.self, from: data)
            return decodedData.last
        } catch {
            print(error)
            return nil
        }
    }
}
